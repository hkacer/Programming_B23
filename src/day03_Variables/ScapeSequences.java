package day03_Variables;

/*
\n:new line
\t: paragraph space (tab)
\": double code:
\\: Backslash



 */

public class ScapeSequences {

    public static void main(String[] args) {




        System.out.println("Hello everyone \nHow are you today");
        System.out.println("Hello This is Kacer. \nI live in Houston. \nI have two kids.");
        System.out.println("----------------------------------------");
        System.out.println("\tToday is a great day to study Java");
        System.out.println("-----------------------------------------");
        System.out.println("My favorite TV series is \"K-Dramas\" ");
        System.out.println("------------------------------------");
        System.out.println("\\");
        System.out.println("-----------------------------");
        System.out.println("\n     ^\n    / \\\n   /   \\\n  /     \\\n /       \\\n ---------");
        System.out.println("---------------------------");
        System.out.println("\n     ^\n    / \\\n   /   \\\n  /     \\\n /       \\\n --------- \n \\       /\n  \\     /\n   \\   /\n    \\ /\n     v ");
        System.out.println("-----------------------------------");
        System.out.println("\n     ^\n    / \\\n   / * \\\n  / * * \\\n / * * * \\\n ---------  \n \\ * * * /\n  \\ * * /\n   \\ * /\n    \\ /\n     v");





    }

}
