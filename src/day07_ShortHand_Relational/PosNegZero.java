package day07_ShortHand_Relational;

public class PosNegZero {

    public static void main(String[] args) {

        int n1= -100;
        boolean isPositive= n1>0;
        // boolean isZero= isNegative==false && isPositive==false;
        boolean isNegative= n1<0;
        boolean isZero= n1==0;
        System.out.println(n1+ " is a Positive number = " + isPositive);
        System.out.println(n1+" is a negative number = " + isNegative);
        System.out.println(n1+ " is Zero = " + isZero);










    }









}
