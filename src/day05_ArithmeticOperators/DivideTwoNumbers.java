package day05_ArithmeticOperators;

public class DivideTwoNumbers {

    public static void main(String[] args) {

        int a= 50;
        int b= 9;
        int result= a/b;
        double rem= a%b;

        System.out.println(a+ " divided by "+ b + " is equal to "+ result+" with a remainder of "+ rem);











    }













}
